    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        try {
            String buttonGroup1 = null;
            if(perempuan.isSelected()){
                buttonGroup1="perempuan";
            }else if(laki.isSelected()){
                buttonGroup1="laki";
            }
          
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/apotek","root","");
            cn.createStatement().executeUpdate("insert into tb_karyawan values ('"
                +id_karyawan.getText()
                +"','"+id_user.getText()
                +"','"+nama_karyawan.getText()
                +"','"+buttonGroup1   
                +"','"+email.getText()
                +"','"+status.getText()
                +"','"+no_tlp.getText()
                +"','"+kewarganegaraan.getText()
                +"','"+pendidikan_terakhir.getText()
                +"','"+alamat.getText()+"')");
            
        Karyawan dtkaryawan = null;
        dtkaryawan = new Karyawan();
        dtkaryawan.setVisible(true);
        dtkaryawan.pack();
        dtkaryawan.setLocationRelativeTo(null);
        this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Data Gagal Ditambahkan");
        }
    }//GEN-LAST:event_tambahActionPerformed

